package org.example.cms;

public class ComparisonOperators {

    public static void main(String[] args) {
        int a = 5;
        int b = 8;

        System.out.println("a এবং b সমান? " + (a == b));//false
        System.out.println("a বড়? " + (a > b));//false
        System.out.println("a ছোট? " + (a < b));//true
    }
}
